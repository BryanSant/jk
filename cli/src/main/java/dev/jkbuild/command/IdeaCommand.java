/**
 * {@code jk idea} — generate IntelliJ IDEA project files ({@code .idea/} +
 * {@code *.iml}) for a workspace or single project.
 *
 * <p>Generated files:
 * <ul>
 *   <li>{@code .idea/modules.xml} — module registry</li>
 *   <li>{@code .idea/misc.xml} — project SDK + language level</li>
 *   <li>{@code .idea/compiler.xml} — per-module output paths</li>
 *   <li>{@code .idea/libraries/*.xml} — one per external dependency;
 *       sources JARs attached when {@code sources-checksum} is in the lock</li>
 *   <li>{@code .idea/runConfigurations/*.xml} — for members with a
 *       {@code [project] main} class</li>
 *   <li>{@code <member>/<module>.iml} — per module: source roots, deps,
 *       scope; workspace siblings become module references</li>
 * </ul>
 *
 * <p>Existing files the command generates are overwritten on each run —
 * {@code jk idea} is idempotent and produces the same output for the same
 * lock. Files not generated by jk (e.g. {@code .idea/workspace.xml},
 * {@code vcs.xml}, hand-crafted run configs) are left untouched.
 */
public final class IdeaCommand implements CliCommand {

    @Override public String name()        { return "idea"; }
    @Override public String description() { return "Generate IntelliJ IDEA project files (.idea/ + *.iml)"; }

    @Override
    public List<Opt> options() {
        return List.of(
                Opt.value("<dir>", "Override the jk cache directory.", "--cache-dir").hide());
    }

    private GlobalOptions global;
    private Path cacheDir;

    @Override
    public int run(Invocation in) throws Exception {
        this.global    = GlobalOptions.from(in);
        this.cacheDir  = in.value("cache-dir").map(Path::of).orElse(null);

        Path startDir = global.workingDir();
        Path cache    = cacheDir != null ? cacheDir : JkDirs.cache();
        Cas  cas      = new Cas(cache);

        // Find workspace root (or treat single project as its own root).
        Path wsRoot;
        JkBuild rootBuild;
        try {
            Path buildFile = startDir.resolve("jk.toml");
            if (!Files.exists(buildFile)) {
                System.err.println("jk idea: no jk.toml in " + startDir);
                return 2;
            }
            rootBuild = JkBuildParser.parse(buildFile);
            if (rootBuild.isWorkspaceRoot()) {
                wsRoot = startDir;
            } else {
                var rootOpt = WorkspaceLocator.findRoot(startDir);
                wsRoot = rootOpt.orElse(startDir);
                rootBuild = JkBuildParser.parse(wsRoot.resolve("jk.toml"));
            }
        } catch (RuntimeException e) {
            System.err.println("jk idea: " + e.getMessage());
            return 2;
        }

        Map<Path, JkBuild> members = rootBuild.isWorkspaceRoot()
                ? WorkspaceLoader.loadMembers(wsRoot, rootBuild)
                : Map.of();

        // Collect all library definitions across all members
        // (name → LibDef). LinkedHashMap preserves stable ordering.
        Map<String, LibDef> allLibs = new LinkedHashMap<>();
        for (Map.Entry<Path, JkBuild> me : members.entrySet()) {
            collectLibDefs(me.getKey(), me.getValue(), members, cas, allLibs);
        }
        if (members.isEmpty()) {
            // Single-project mode — treat the root as the only member.
            collectLibDefs(wsRoot, rootBuild, Map.of(), cas, allLibs);
        }

        // ---- generate .idea/ -----------------------------------------------
        Path ideaDir = wsRoot.resolve(".idea");
        Files.createDirectories(ideaDir);

        int files = 0;
        files += writeModulesXml(ideaDir, wsRoot, rootBuild, members);
        files += writeMiscXml(ideaDir, wsRoot, rootBuild, members);
        files += writeCompilerXml(ideaDir, wsRoot, rootBuild, members);

        Path libsDir = ideaDir.resolve("libraries");
        Files.createDirectories(libsDir);
        for (LibDef lib : allLibs.values()) {
            write(libsDir.resolve(lib.fileName + ".xml"), libraryXml(lib, wsRoot, cas));
            files++;
        }

        Path runDir = ideaDir.resolve("runConfigurations");
        for (Map.Entry<Path, JkBuild> me : members.entrySet()) {
            String main = me.getValue().project().main();
            if (main != null && !main.isBlank()) {
                Files.createDirectories(runDir);
                String modName = moduleName(me.getValue());
                write(runDir.resolve(sanitize(modName) + ".xml"),
                        runConfigXml(modName, main));
                files++;
            }
        }
        // Single-project run config
        if (members.isEmpty() && rootBuild.project().main() != null
                && !rootBuild.project().main().isBlank()) {
            Files.createDirectories(runDir);
            String modName = moduleName(rootBuild);
            write(runDir.resolve(sanitize(modName) + ".xml"),
                    runConfigXml(modName, rootBuild.project().main()));
            files++;
        }

        // ---- generate *.iml for each member --------------------------------
        Iterable<Map.Entry<Path, JkBuild>> targets = members.isEmpty()
                ? List.of(Map.entry(wsRoot, rootBuild))
                : members.entrySet();

        for (Map.Entry<Path, JkBuild> me : targets) {
            Path memberDir = me.getKey();
            JkBuild member = me.getValue();
            List<ModuleRef> modRefs = siblingModuleRefs(memberDir, member, members);
            List<LibRef>    libRefs = libRefs(memberDir, member, members, cas, allLibs);
            write(memberDir.resolve(moduleName(member) + ".iml"),
                    imlXml(memberDir, member, modRefs, libRefs));
            files++;
        }

        System.out.println("✓ Generated " + files + " IntelliJ project file"
                + (files == 1 ? "" : "s") + " in " + wsRoot.getFileName() + "/.idea/");
        System.out.println("  Open " + wsRoot.getFileName() + "/ in IntelliJ IDEA.");
        return 0;
    }

    // =========================================================================
    // Library collection
    // =========================================================================

    /** Collect all external (non-workspace) dep library definitions for one member. */
    private static void collectLibDefs(Path memberDir, JkBuild member,
                                        Map<Path, JkBuild> members, Cas cas,
                                        Map<String, LibDef> allLibs) throws IOException {
        Path lockFile = memberDir.resolve("jk.lock");
        if (!Files.exists(lockFile)) return;
        Lockfile lock = LockfileReader.read(lockFile);

        // Fetch any missing JARs so the library XML has valid CAS paths.
        // This mirrors `jk sync` — `jk idea` should work without a prior sync.
        try {
            new CacheSync(cas, new Http()).sync(lock, CacheSync.ProgressObserver.NOOP);
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
        } catch (Exception ignored) { /* best-effort; missing JARs are skipped below */ }

        // Build a set of workspace-sibling coordinates so we can skip them.
        Set<String> siblingCoords = siblingCoordinates(member, members);

        for (Lockfile.Package pkg : lock.packages()) {
            if (pkg.checksum() == null) continue;            // path/git dep
            if (siblingCoords.contains(pkg.name())) continue; // workspace sibling → module dep
            if (allLibs.containsKey(pkg.name() + ":" + pkg.version())) continue;

            String hex = hexOf(pkg.checksum());
            if (hex == null || !cas.contains(hex)) continue; // not yet synced

            Path jarPath     = cas.pathFor(hex);
            Path sourcesPath = null;
            if (pkg.sourcesChecksum() != null) {
                String srcHex = hexOf(pkg.sourcesChecksum());
                if (srcHex != null && cas.contains(srcHex)) {
                    sourcesPath = cas.pathFor(srcHex);
                }
            }

            String libName = pkg.name() + ":" + pkg.version();
            allLibs.put(libName, new LibDef(libName, libFileName(libName),
                    jarPath, sourcesPath));
        }
    }

    // =========================================================================
    // Per-module dependency lists
    // =========================================================================

    /** Workspace siblings this module directly depends on (MAIN + TEST scopes). */
    private static List<ModuleRef> siblingModuleRefs(Path memberDir, JkBuild member,
                                                      Map<Path, JkBuild> members)
            throws IOException {
        List<ModuleRef> result = new ArrayList<>();
        WorkspaceClasspath.Result mainCp =
                WorkspaceClasspath.resolve(memberDir, member, EnumSet.of(Scope.MAIN));
        WorkspaceClasspath.Result testCp =
                WorkspaceClasspath.resolve(memberDir, member, EnumSet.of(Scope.TEST));

        // Map jar → module name for all workspace siblings
        Map<Path, String> jarToModule = new LinkedHashMap<>();
        for (Map.Entry<Path, JkBuild> me : members.entrySet()) {
            BuildLayout layout = BuildLayout.of(me.getKey(), me.getValue());
            jarToModule.put(layout.mainJar(), moduleName(me.getValue()));
        }

        Set<String> added = new LinkedHashSet<>();
        for (Path sj : mainCp.jars()) {
            String name = jarToModule.get(sj);
            if (name != null && added.add(name))
                result.add(new ModuleRef(name, "COMPILE"));
        }
        for (Path sj : testCp.jars()) {
            String name = jarToModule.get(sj);
            if (name != null && added.add(name))
                result.add(new ModuleRef(name, "TEST"));
        }
        return result;
    }

    /** External library references (scope-tagged) for one module. */
    private static List<LibRef> libRefs(Path memberDir, JkBuild member,
                                         Map<Path, JkBuild> allMembers, Cas cas,
                                         Map<String, LibDef> allLibs) throws IOException {
        Path lockFile = memberDir.resolve("jk.lock");
        if (!Files.exists(lockFile)) return List.of();
        Lockfile lock = LockfileReader.read(lockFile);
        Set<String> siblingCoords = siblingCoordinates(member, allMembers);

        List<LibRef> result = new ArrayList<>();
        for (Lockfile.Package pkg : lock.packages()) {
            if (pkg.checksum() == null) continue;
            if (siblingCoords.contains(pkg.name())) continue;
            String libName = pkg.name() + ":" + pkg.version();
            if (!allLibs.containsKey(libName)) continue;
            result.add(new LibRef(libName, ideaScope(pkg.scopes())));
        }
        return result;
    }

    // =========================================================================
    // XML generators
    // =========================================================================

    private static int writeModulesXml(Path ideaDir, Path wsRoot,
                                        JkBuild root, Map<Path, JkBuild> members)
            throws IOException {
        StringBuilder sb = xmlHeader();
        sb.append("<project version=\"4\">\n");
        sb.append("  <component name=\"ProjectModuleManager\">\n");
        sb.append("    <modules>\n");

        Iterable<Map.Entry<Path, JkBuild>> targets = members.isEmpty()
                ? List.of(Map.entry(wsRoot, root)) : members.entrySet();

        for (Map.Entry<Path, JkBuild> me : targets) {
            Path iml = me.getKey().resolve(moduleName(me.getValue()) + ".iml");
            String rel = "$PROJECT_DIR$/" + wsRoot.relativize(iml).toString().replace('\\', '/');
            sb.append("      <module fileurl=\"file://").append(rel)
              .append("\" filepath=\"").append(rel).append("\" />\n");
        }
        sb.append("    </modules>\n  </component>\n</project>\n");
        write(ideaDir.resolve("modules.xml"), sb.toString());
        return 1;
    }

    private static int writeMiscXml(Path ideaDir, Path wsRoot,
                                     JkBuild root, Map<Path, JkBuild> members)
            throws IOException {
        // Use the root jdk level, fall back to highest member level.
        int jdk = root.project().jdk();
        if (jdk == 0) {
            for (JkBuild m : members.values()) jdk = Math.max(jdk, m.project().jdk());
        }
        if (jdk == 0) jdk = 21;

        StringBuilder sb = xmlHeader();
        sb.append("<project version=\"4\">\n");
        sb.append("  <component name=\"ProjectRootManager\" version=\"2\"");
        sb.append(" languageLevel=\"JDK_").append(jdk).append("\"");
        sb.append(" project-jdk-name=\"").append(jdk).append("\"");
        sb.append(" project-jdk-type=\"JavaSDK\">\n");
        sb.append("    <output url=\"file://$PROJECT_DIR$/out\" />\n");
        sb.append("  </component>\n</project>\n");
        write(ideaDir.resolve("misc.xml"), sb.toString());
        return 1;
    }

    private static int writeCompilerXml(Path ideaDir, Path wsRoot,
                                         JkBuild root, Map<Path, JkBuild> members)
            throws IOException {
        StringBuilder sb = xmlHeader();
        sb.append("<project version=\"4\">\n");
        sb.append("  <component name=\"CompilerConfiguration\">\n");
        sb.append("    <bytecodeTargetLevel>\n");

        Iterable<Map.Entry<Path, JkBuild>> targets = members.isEmpty()
                ? List.of(Map.entry(wsRoot, root)) : members.entrySet();

        for (Map.Entry<Path, JkBuild> me : targets) {
            int release = me.getValue().project().javaRelease();
            if (release > 0) {
                sb.append("      <module name=\"").append(esc(moduleName(me.getValue())))
                  .append("\" targetLevel=\"").append(release).append("\" />\n");
            }
        }
        sb.append("    </bytecodeTargetLevel>\n  </component>\n</project>\n");
        write(ideaDir.resolve("compiler.xml"), sb.toString());
        return 1;
    }

    private static String libraryXml(LibDef lib, Path wsRoot, Cas cas) {
        StringBuilder sb = xmlHeader();
        sb.append("<component name=\"libraryTable\">\n");
        sb.append("  <library name=\"").append(esc(lib.name)).append("\">\n");
        sb.append("    <CLASSES>\n");
        sb.append("      <root url=\"").append(jarUrl(lib.jarPath)).append("\" />\n");
        sb.append("    </CLASSES>\n");
        sb.append("    <JAVADOC />\n");
        sb.append("    <SOURCES>\n");
        if (lib.sourcesPath != null) {
            sb.append("      <root url=\"").append(jarUrl(lib.sourcesPath)).append("\" />\n");
        }
        sb.append("    </SOURCES>\n");
        sb.append("  </library>\n</component>\n");
        return sb.toString();
    }

    private static String imlXml(Path memberDir, JkBuild member,
                                   List<ModuleRef> modRefs, List<LibRef> libRefs) {
        StringBuilder sb = xmlHeader();
        sb.append("<module type=\"JAVA_MODULE\" version=\"4\">\n");
        sb.append("  <component name=\"NewModuleRootManager\" inherit-compiler-output=\"false\">\n");

        BuildLayout layout = BuildLayout.of(memberDir, member);
        sb.append("    <output url=\"file://$MODULE_DIR$/")
          .append(memberDir.relativize(layout.classesDir()).toString().replace('\\', '/'))
          .append("\" />\n");
        sb.append("    <output-test url=\"file://$MODULE_DIR$/")
          .append(memberDir.relativize(layout.testClassesDir()).toString().replace('\\', '/'))
          .append("\" />\n");
        sb.append("    <exclude-output />\n");

        // Source + test folders.
        // Traditional layout (Maven convention) takes precedence over simple layout:
        // only add "src" / "test" if no traditional source dirs exist.
        sb.append("    <content url=\"file://$MODULE_DIR$\">\n");
        boolean hasTraditional =
                Files.isDirectory(memberDir.resolve("src/main/java")) ||
                Files.isDirectory(memberDir.resolve("src/main/kotlin")) ||
                Files.isDirectory(memberDir.resolve("src/test/java"))  ||
                Files.isDirectory(memberDir.resolve("src/test/kotlin"));
        if (hasTraditional) {
            addSourceFolder(sb, memberDir, "src/main/java",   false);
            addSourceFolder(sb, memberDir, "src/main/kotlin", false);
            addSourceFolder(sb, memberDir, "src/test/java",   true);
            addSourceFolder(sb, memberDir, "src/test/kotlin", true);
        } else {
            // simple layout
            addSourceFolder(sb, memberDir, "src",  false);
            addSourceFolder(sb, memberDir, "test", true);
        }
        // exclude build outputs
        sb.append("      <excludeFolder url=\"file://$MODULE_DIR$/target\" />\n");
        sb.append("    </content>\n");

        sb.append("    <orderEntry type=\"inheritedJdk\" />\n");
        sb.append("    <orderEntry type=\"sourceFolder\" forTests=\"false\" />\n");

        // Workspace-sibling module dependencies
        for (ModuleRef mr : modRefs) {
            sb.append("    <orderEntry type=\"module\" module-name=\"")
              .append(esc(mr.name)).append("\"");
            if ("TEST".equals(mr.scope)) sb.append(" scope=\"TEST\"");
            sb.append(" />\n");
        }

        // External library references
        for (LibRef lr : libRefs) {
            sb.append("    <orderEntry type=\"library\" name=\"")
              .append(esc(lr.name)).append("\" level=\"project\"");
            if (!"COMPILE".equals(lr.scope))
                sb.append(" scope=\"").append(lr.scope).append("\"");
            sb.append(" />\n");
        }

        sb.append("  </component>\n</module>\n");
        return sb.toString();
    }

    private static String runConfigXml(String moduleName, String mainClass) {
        StringBuilder sb = xmlHeader();
        sb.append("<component name=\"ProjectRunConfigurationManager\">\n");
        sb.append("  <configuration default=\"false\" name=\"")
          .append(esc(moduleName)).append("\" type=\"Application\" factoryName=\"Application\">\n");
        sb.append("    <option name=\"MAIN_CLASS_NAME\" value=\"")
          .append(esc(mainClass)).append("\" />\n");
        sb.append("    <module name=\"").append(esc(moduleName)).append("\" />\n");
        sb.append("    <method v=\"2\">\n");
        sb.append("      <option name=\"Make\" enabled=\"true\" />\n");
        sb.append("    </method>\n");
        sb.append("  </configuration>\n</component>\n");
        return sb.toString();
    }

    // =========================================================================
    // Helpers
    // =========================================================================

    private static void addSourceFolder(StringBuilder sb, Path memberDir,
                                         String relative, boolean test) {
        Path dir = memberDir.resolve(relative);
        if (!Files.isDirectory(dir)) return;
        sb.append("      <sourceFolder url=\"file://$MODULE_DIR$/")
          .append(relative).append("\" isTestSource=\"").append(test).append("\" />\n");
    }

    /** The IntelliJ module name for a workspace member. */
    private static String moduleName(JkBuild member) {
        return member.project().name();
    }

    /** Sanitize a string to a valid filename component. */
    private static String sanitize(String s) {
        return s.replaceAll("[^A-Za-z0-9._-]", "_");
    }

    /** Library filename (no extension). Replaces non-safe chars with underscores. */
    private static String libFileName(String libName) {
        return sanitize(libName.replace(':', '_').replace('.', '_'));
    }

    /**
     * Convert an absolute JAR path to IntelliJ's {@code jar://...!/} URL format.
     * Paths under the user's home directory use {@code $USER_HOME$}.
     */
    private static String jarUrl(Path jar) {
        String abs = jar.toAbsolutePath().normalize().toString().replace('\\', '/');
        String home = System.getProperty("user.home", "").replace('\\', '/');
        if (!home.isBlank() && abs.startsWith(home + "/")) {
            abs = "$USER_HOME$/" + abs.substring(home.length() + 1);
        }
        return "jar://" + abs + "!/";
    }

    /** Extract the raw hex SHA from a {@code "sha256:<hex>"} checksum string. */
    private static String hexOf(String checksum) {
        if (checksum == null) return null;
        return checksum.startsWith("sha256:") ? checksum.substring(7) : checksum;
    }

    /** Map jk scopes to the most permissive IntelliJ scope. */
    private static String ideaScope(List<dev.jkbuild.model.Scope> scopes) {
        Set<dev.jkbuild.model.Scope> s = EnumSet.noneOf(dev.jkbuild.model.Scope.class);
        s.addAll(scopes);
        if (s.contains(Scope.EXPORT) || s.contains(Scope.MAIN)) return "COMPILE";
        if (s.contains(Scope.PROVIDED))                          return "PROVIDED";
        if (s.contains(Scope.RUNTIME))                          return "RUNTIME";
        if (s.contains(Scope.TEST))                             return "TEST";
        return "COMPILE";
    }

    /** Coordinates of all workspace siblings that this member could declare as deps. */
    private static Set<String> siblingCoordinates(JkBuild member,
                                                    Map<Path, JkBuild> allMembers) {
        Set<String> coords = new LinkedHashSet<>();
        for (JkBuild sib : allMembers.values()) {
            if (sib != member) {
                coords.add(sib.project().group() + ":" + sib.project().name());
            }
        }
        return coords;
    }

    private static StringBuilder xmlHeader() {
        StringBuilder sb = new StringBuilder();
        sb.append("<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n");
        return sb;
    }

    /** XML-escape a string for attribute values. */
    private static String esc(String s) {
        return s.replace("&", "&amp;").replace("\"", "&quot;")
                .replace("<", "&lt;").replace(">", "&gt;");
    }

    private static void write(Path file, String content) throws IOException {
        Files.writeString(file, content, StandardCharsets.UTF_8);
    }

    // =========================================================================
    // Data records
    // =========================================================================

    private record LibDef(String name, String fileName, Path jarPath, Path sourcesPath) {}
    private record LibRef(String name, String scope) {}
    private record ModuleRef(String name, String scope) {}
}
